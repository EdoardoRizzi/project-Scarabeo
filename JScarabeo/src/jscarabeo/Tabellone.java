    private String generaNome(Lettera l, String NomeAttuale){
        String nome = NomeAttuale;
        if (nome.contains("$")) {
            int pos = nome.indexOf("$");
            nome = nome.substring(0, pos);
        } else {
            nome += "$" + Character.toString(l.getLettera());
        }
        
        return nome;
    }

